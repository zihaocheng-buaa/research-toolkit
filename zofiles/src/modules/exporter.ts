let _exporter: Exporter | null = null;

/**
 * Get the singleton Exporter instance.
 */
export function getExporter(): Exporter {
  if (!_exporter) {
    _exporter = new Exporter();
  }
  return _exporter;
}

/**
 * Reset the singleton (used during plugin shutdown or testing).
 */
export function resetExporter(): void {
  _exporter = null;
}
